/** Current language, persisted across reloads. */
let currentLang = localStorage.getItem('drape-lang') || 'en';

function t(key){
  return (I18N[currentLang] && I18N[currentLang][key]) || I18N.en[key] || key;
}

function applyTranslations(){
  document.documentElement.lang = currentLang === 'km' ? 'km' : 'en';
  document.body.classList.toggle('lang-km', currentLang === 'km');
  document.querySelectorAll('[data-i18n]').forEach(el=>{
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el=>{
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
}

function setLang(lang){
  currentLang = lang;
  localStorage.setItem('drape-lang', lang);
  applyTranslations();
  document.querySelectorAll('.lang-btn').forEach(b=>{
    b.classList.toggle('active', b.dataset.lang === lang);
  });
  document.dispatchEvent(new CustomEvent('lang-changed'));
}
